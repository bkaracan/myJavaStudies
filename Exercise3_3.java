public class Exercise3_3 {

	public static void main(String[] args) {
		
		int x = 5;
		
		x = x + 1; // x = 6
		
		x += 1; // x = 7
		
		x ++; // x = 8
		
		++ x; // x = 9
		
		System.out.println(x);

	}

}
